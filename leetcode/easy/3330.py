from typing import List
from collections import defaultdict

# Definition for singly-linked list.
class ListNode:
    def __init__(self, val=0, next=None):
        self.val = val
        self.next = next

class Solution:
    def leetcode(self, word: str) -> int:
        ll = len(word)
        count = 1
        result = 1
        for ii in range(1, ll):
            if word[ii] == word[ii-1]:
                count += 1
            else:
                result += count-1
                count = 1
        
        result += count-1

        return result


