class Solution:
    def leetcode(self, s: str) -> int:
        q = ["a"]
        for each in s:
            if each == "B" and q[-1] == "A":
                q.pop()
                continue
            if each == "D" and q[-1] == "C":
                q.pop()
                continue
            q.append(each)

        return len(q)-1

